package String_methods;

public class S005_contains {
public static void main(String[] args) {
	
	//contains compares if a string method contains a string or not returns true and false
	String s = "Hello";
	
	System.out.println(s.contains("Hel"));

	System.out.println(s.contains("e"));

	System.out.println(s.contains("Je"));
	
	//output
//	true
//	true
//	false

	
}
}
	